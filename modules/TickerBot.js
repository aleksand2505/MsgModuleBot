var TickerBot = async function(context, pref_ticker) {
if (context.text.startsWith(pref_ticker)) {
    a = context.text.split('')
    a.shift()
    if (a.length > 15) return
    for (var i = 0; i < a.length; i++) {
        if (!Object.keys(letter).includes(a[i])) return
        context.editMessage({
            message: letter[a[i]]
        })
        await timer(1200);
      }
      context.deleteMessage({
        message_ids: context.id,
        delete_for_all: 1
    })
}
}




function timer(ms) {
   return new Promise(res => setTimeout(res, ms));
}
